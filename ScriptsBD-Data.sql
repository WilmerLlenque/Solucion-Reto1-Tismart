-- -----------------------------------------------------
-- Table "GERENTE"
-- -----------------------------------------------------

CREATE TABLE GERENTE(
    id_gerente NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    desc_gerente VARCHAR2(150) NOT NULL,
    fecha_registro DATE NOT NULL,
    PRIMARY KEY (id_gerente)
);

-- -----------------------------------------------------
-- Table "CONDICION"
-- -----------------------------------------------------

CREATE TABLE CONDICION(
    id_condicion NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    desc_condicion VARCHAR2(150) NOT NULL,
    fecha_registro DATE NOT NULL,
    PRIMARY KEY (id_condicion)
);

-- -----------------------------------------------------
-- Table "SEDE"
-- -----------------------------------------------------

CREATE TABLE SEDE(
    id_sede NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    desc_sede VARCHAR2(150) NOT NULL,
    fecha_registro DATE NOT NULL,
    PRIMARY KEY (id_sede)
);

-- -----------------------------------------------------
-- Table "PROVINCIA"
-- -----------------------------------------------------

CREATE TABLE PROVINCIA(
    id_provincia NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    desc_provincia VARCHAR2(150) NOT NULL,
    fecha_registro DATE NOT NULL,
    PRIMARY KEY (id_provincia)
);

-- -----------------------------------------------------
-- Table "DISTRITO"
-- -----------------------------------------------------

CREATE TABLE DISTRITO(
    id_distrito NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    id_provincia NUMBER NOT NULL,
    desc_distrito VARCHAR2(550) NOT NULL,
    fecha_registro DATE NOT NULL,
    PRIMARY KEY (id_distrito),
    CONSTRAINT fk_DISTRITO_PROVINCIA
        FOREIGN KEY (id_provincia)
        REFERENCES PROVINCIA (id_provincia)
);

-- -----------------------------------------------------
-- Table "HOSPITAL"
-- -----------------------------------------------------

CREATE TABLE HOSPITAL(
    id_hospital NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    id_distrito NUMBER NOT NULL,
    nombre VARCHAR2(100) NOT NULL,
    antiguedad NUMBER NOT NULL,
    area NUMBER(5,2) NOT NULL,
    id_sede NUMBER NOT NULL,
    id_gerente NUMBER NOT NULL,
    id_condicion NUMBER NOT NULL,
    fecha_registro DATE NOT NULL,
    PRIMARY KEY (id_hospital),
    CONSTRAINT fk_HOSPITAL_DISTRITO
        FOREIGN KEY (id_distrito)
        REFERENCES DISTRITO (id_distrito),
    CONSTRAINT fk_HOSPITAL_SEDE
        FOREIGN KEY (id_sede)
        REFERENCES SEDE (id_sede),
    CONSTRAINT fk_HOSPITAL_GERENTE
        FOREIGN KEY (id_gerente)
        REFERENCES GERENTE (id_gerente),
    CONSTRAINT fk_HOSPITAL_CONDICION
        FOREIGN KEY (id_condicion)
        REFERENCES CONDICION (id_condicion)

);


